export const addTwoNumbers = (num1:number, num2:number = 0):number => { 
    return num1 + num2;
}
export const SubstractTwoNumbers = (num1:number, num2:number = 0):number => { 
    return num1 - num2;
}
export const TimesTwoNumbers = (num1:number, num2:number = 0):number => { 
    return num1 * num2;
}
export const DivideTwoNumbers = (num1:number, num2:number = 0):number => { 
    return num1 / num2;
}